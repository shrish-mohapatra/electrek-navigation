export const generatePositions = (route, start) => {
    // Take route string (ex. "ulllpd") and return array of positions

    const move_map = {
        'u': [-1, 0],
        'd': [1, 0],
        'l': [0, -1],
        'r': [0, 1],
        'c': [0,0],
    }

    let positions = [start]

    for(let m in route) {
        const vect = move_map[route[m]]
        start = [start[0] + vect[0], start[1] + vect[1]]
        positions.push(start)
    }

    return positions
}